"""IPC-D-356 netlist export — the electrical test file (KiCad-map tier 2).

Flying-probe and bed-of-nails testers consume this: every netted pad and
via as a fixed-column record (317 = through-hole, 327 = SMD) with net,
refdes, pin, position, and size. Layout follows the published record
format (as KiCad emits it); metric units record, coordinates in microns.

Honesty note: structurally verified against the record layout; not yet
conformance-run on a physical tester — treat as 'structure emitted'.
"""

from __future__ import annotations

from gitcad.ecad.board import Board


def _num(v_mm: float, width: int = 6) -> str:
    """Signed fixed-width micron field: +012345 style."""
    n = round(v_mm * 1000)
    return ("+" if n >= 0 else "-") + str(abs(n)).rjust(width, "0")


def to_ipcd356(board: Board) -> str:
    lines = [
        "C  IPC-D-356 generated by gitcad",
        f"P  JOB   {board.name}",
        "P  UNITS CUST 1",          # metric: mm/microns
    ]
    for comp in board.components:
        for pad, bx, by, rot in comp.placed_pads():
            net = comp.nets.get(pad.name, "")
            if not net:
                continue
            record = "317" if pad.drill is not None else "327"
            hole = f"D{round(pad.drill * 1000):04d}" if pad.drill is not None else "     "
            access = "A00" if comp.side == "top" else "A01"
            w, h = (pad.h, pad.w) if round(rot) % 180 == 90 else (pad.w, pad.h)
            lines.append(
                f"{record}{net[:14].ljust(14)} {comp.ref[:6].ljust(6)}-{pad.name[:4].ljust(4)}"
                f"{hole}{access}X{_num(bx)}Y{_num(by)}"
                f"X{round(w * 1000):04d}Y{round(h * 1000):04d}")
    for i, via in enumerate(board.vias):
        if not via.net:
            continue
        lines.append(
            f"317{via.net[:14].ljust(14)} VIA   -{str(i)[:4].ljust(4)}"
            f"D{round(via.drill * 1000):04d}A00X{_num(via.x)}Y{_num(via.y)}"
            f"X{round(via.diameter * 1000):04d}Y{round(via.diameter * 1000):04d}")
    lines.append("999")
    return "\n".join(lines) + "\n"
